#!/bin/bash

# 電車の絵を描く関数
draw_train() {
    cat << "EOF"
                     ( )   (@@)   ( )   (@)   ( )  (@@)    0    (@@)   0    (@)
                  (   )                       (   )   
                (@@@)                        (@@@@)
             (@@@@)                       (@@@@)
           (     )                     (     )

        (@@@)                        (@@@@)
   ====         _________                ___________
  _D_|_|_______|        \_____I_I______===|_____|______|_
 |  (_)---  |   H\\________/ |   |         =| ___ _____|  |
/     |     |   H  |  |     |   |          ||_| |_||     |
|     |     |   H  |__----------------------| [___] |    |
| _________|___H__/__|____/[][]~\_______|        |    |
|/ |   |-----------I_____I [][] []  D   |=======|____|__
__/ =| o |=-~~\  /~~\  /~~\  /~~\  ____Y___________|___
 |/-=|___|=    ||    ||    ||    |_____/~\___/        |
  \_/      \O=====O=====O=====O_/      \_/            |
                                                       |
EOF
}

# 蒸気を表現するアニメーション関数
animate_steam() {
    local frames=(
        "   (   )  "
        "  (@@)   "
        " (@@@)   "
        "(@@@@)   "
        "(     )  "
    )

    for i in {1..20}; do
        for frame in "${frames[@]}"; do
            clear
            echo -e "\n"
            draw_train
            echo -e "\033[2A\033[55C$frame"
            sleep 0.3
        done
    done
}

# メイン処理
clear
animate_steam
