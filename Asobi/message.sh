#!/bin/bash
# ------------------------------------
# 💖 シェルスクリプト応援団 v2.0
# ------------------------------------

# カラー設定
PINK='\033[1;35m'
CYAN='\033[0;36m'
YELLOW='\033[1;33m'
RESET='\033[0m'

# 応援メッセージ一覧
messages=(
    "どんな日でも、あなたの努力はちゃんと輝いてるにゃ✨"
    "焦らなくて大丈夫。ゆっくりでも、ちゃんと前に進んでるよ🌸"
    "あなたのコード、今日も動いてる。すごいことだよ💻"
    "ミスもバグも、成長の証だにゃ🐾"
    "笑って、深呼吸して、もう一度やってみよう☀️"
    "どんなときも、あなたの味方だよ💖"
    "小さな成功を、ちゃんと自分で褒めてあげてね🌼"
)

# ランダムで応援メッセージを選ぶ
msg="${messages[$RANDOM % ${#messages[@]}]}"

# アニメーション演出
echo -e "${CYAN}💫 シェルスクリプトがあなたを応援中..."
for i in {1..3}; do
    echo -n "✨"
    sleep 0.4
done
echo -e "\n"

# メッセージ表示
echo -e "${PINK}$msg${RESET}"
echo -e "${YELLOW}💖 Always with you — from your Shell 💖${RESET}\n"
