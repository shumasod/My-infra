#!/bin/bash

draw_train() {
    cat << "EOF"
                    ( ) (@@) ( ) (@) ()  @@    0    @    0     @
               (   )
           (@@@)
        (@@@@)
      (    )

     (@@@)
  ====        _________                ___________
  _D_|_|______|        \_____I_I_____===__|___________|_
 |(_)---  |   H\\________/ |   |        =|___ _____|   |
 /     |  |   H  |  |     |   |         ||_| |_||     |
|      |  |   H  |__--------------------| [___] |     |
| ________|___H__/__|_____/[][]~\_______|       |     |
|/ |   |-----------I_____I [][] []  D   |=======|____|_
__/ =| o |=-~~\  /~~\  /~~\  /~~\ ____Y___________|__
 |/-=|___|=    ||    ||    ||    |_____/~\___/        |
  \_/      \O=====O=====O=====O_/      \_/            |
                                                       |
EOF
}

# 蒸気を表現するアニメーション関数
animate_steam() {
    local frames=(
        "    ( )  "
        "   (@@)  "
        "  (@@@)  "
        " (@@@@)  "
        "(     )  "
    )
    
    for i in {1..20}; do
        for frame in "${frames[@]}"; do
            clear
            echo -e "\n"
            draw_train
            echo -e "\033[2A\033[55C$frame"
            sleep 0.3
        done
    done
}

# メイン処理
clear
animate_steam