#!/bin/bash

# 文字色を設定
GREEN='\033[0;32m'
BROWN='\033[0;33m'
RED='\033[0;31m'
WHITE='\033[1;37m'
NC='\033[0m' # No Color

# 門松のアスキーアート
cat << "EOF"
${RED}    ❀ ${WHITE}扇${RED} ❀
${GREEN}   /|||\\${RED} *
  //|||\\\\
 ///|||\\\\\ 
${WHITE}    |||
    |||    
${GREEN}  \\ ||| /
   \\|||/
    |||
${BROWN}===========
\\\\\\|||||////
 \\\\|||////
  \\|||//
   \\|//
EOF
echo -e "${NC}"

# 新年の挨拶
echo "明けましておめでとうございます"
echo "謹んで新年のお慶びを申し上げます"

# 現在の年を取得して表示
YEAR=$(date +%Y)
echo ""
echo "$YEAR年も宜しくお願いいたします"
