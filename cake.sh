#!/bin/bash

# ケーキ作りの材料準備
echo "ケーキ作りの材料を準備します..."

# 材料リスト
materials=("小麦粉" "バター" "砂糖" "卵" "ベーキングパウダー" "バニラエッセンス" "生クリーム" "フルーツ")

for item in "${materials[@]}"; do
    echo "$item を準備中..."
done

# ケーキを焼く手順
echo "オーブンを180℃に予熱します..."
sleep 2
echo "生地を混ぜてケーキ型に流し込みます..."
sleep 2
echo "オーブンで30～35分間焼きます..."
sleep 10  # 実際の焼き時間は短縮しています
echo "ケーキが冷めたら、生クリームとフルーツでデコレーションします。"

echo "ケーキが完成しました！"