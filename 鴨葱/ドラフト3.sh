#!/bin/bash

# 鴨葱うどんを表示する関数
function show_kamonegi() {
  # 引数でうどんの種類を取得
  udon_type=$1

  echo "**********************************"
  echo "** 鴨葱うどん ($udon_type) **"
  echo "**********************************"

  # うどんの種類によって具材や作り方を変える

  case $udon_type in
    "讃岐うどん")
      echo "  具材："
      echo "    - 讃岐うどん"
      echo "    - 鴨肉"
      echo "    - 九条ネギ"
      echo "    - だし汁"

      echo "  作り方："
      echo "  1. 鍋にだし汁を入れて沸騰させる。"
      echo "  2. 讃岐うどんを袋から取り出し、鍋に入れる。"
      echo "  3. うどんが柔らかくなったら、鴨肉と九条ネギを加える。"
      echo "  4. 温まったら、器に盛り付けて完成。"
      ;;
    "稲庭うどん")
      echo "  具材："
      echo "    - 稲庭うどん"
      echo "    - 鴨肉"
      echo "    - 三つ葉"
      echo "    - だし汁"

      echo "  作り方："
      echo "  1. 鍋にだし汁を入れて沸騰させる。"
      echo "  2. 稲庭うどんを袋から取り出し、鍋に入れる。"
      echo "  3. うどんが柔らかくなったら、鴨肉と三つ葉を加える。"
      echo "  4. 温まったら、器に盛り付けて完成。"
      ;;
    *)
      echo "  うどんの種類が不明です。"
      ;;
  esac

  echo "**********************************"
}

# 引数でうどんの種類を指定
show_kamonegi "讃岐うどん"
